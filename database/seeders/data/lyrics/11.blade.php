<p>Emlékezzél meg, te gyarló ember a te Istenedről,<br>
Háládatlan te soha ne légy a te Teremtődhöz!</p>

<p>Lám a madarak, mely korán reggel énekelni kezdnek,<br>
Amire Isten őket teremté, el nem felejtkeznek.</p>

<p>Te pedig ember úgy teremttettél az ő szent képére,<br>
Tenálad vagyon az ő szájából kieresztett lelke.</p>

<p>Megékesített téged az Isten nyelved szólásával,<br>
És felépített téged az Isten lelki okossággal.</p>

<p>Ne tedd tunyává azért elmédet, dicsérd Istenedet,<br>
Mert ezt kívánja ő csak tetőled, áldjad Teremtődet!</p>
